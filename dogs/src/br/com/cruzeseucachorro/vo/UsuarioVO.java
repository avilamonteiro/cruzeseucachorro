package br.com.cruzeseucachorro.vo;

public class UsuarioVO {

	private String nome;
	private String sobreNome;
	private String dataNascimento;
	private String email;
	private String loginNome;
	private String loginSenha;
	private String loginPerguntaSenha;
	private String loginRespostaSenha;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getSobreNome() {
		return sobreNome;
	}

	public void setSobreNome(String sobreNome) {
		this.sobreNome = sobreNome;
	}

	public String getDataNascimento() {
		return dataNascimento;
	}

	public void setDataNascimento(String dataNascimento) {
		this.dataNascimento = dataNascimento;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getLoginNome() {
		return loginNome;
	}

	public void setLoginNome(String loginNome) {
		this.loginNome = loginNome;
	}

	public String getLoginSenha() {
		return loginSenha;
	}

	public void setLoginSenha(String loginSenha) {
		this.loginSenha = loginSenha;
	}

	public String getLoginPerguntaSenha() {
		return loginPerguntaSenha;
	}

	public void setLoginPerguntaSenha(String loginPerguntaSenha) {
		this.loginPerguntaSenha = loginPerguntaSenha;
	}

	public String getLoginRespostaSenha() {
		return loginRespostaSenha;
	}

	public void setLoginRespostaSenha(String loginRespostaSenha) {
		this.loginRespostaSenha = loginRespostaSenha;
	}

}
